mood = input("What is your mood? :")
mood = mood.lower()

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Cheer up, mate!")
elif mood == "excited":
    print("Going great!")
elif mood == "relaxed":
    print("Taking it slow")
else:
    print("I don't recognize this mood")
